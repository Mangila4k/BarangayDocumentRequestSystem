    private void saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveMouseClicked
         dbConnector dbc = new dbConnector();

    if (cfname.getText().isEmpty() || clname.getText().isEmpty() || cemail.getText().isEmpty() ||
        caddress.getText().isEmpty() || cstatus.getText().isEmpty() || ccontacts.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please fill in all fields!", "Validation Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (action.equals("Add")) {
        String sql = "INSERT INTO tbl_citizen (c_fname, c_lname, c_gender, c_email, c_address, c_status, c_contact) " +
                     "VALUES ('" + cfname.getText() + "', '" + clname.getText() + "', '" + 
                     cgender.getSelectedItem().toString() + "', '" + cemail.getText() + "', '" + 
                     caddress.getText() + "', '" + cstatus.getText() + "', '" + ccontacts.getText() + "')";

        int result = dbc.insertData(sql);

        if (result == 1) {
            JOptionPane.showMessageDialog(null, "Citizen added successfully!");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Error saving data!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } 
    else if (action.equals("Update")) {
        if (cid.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Error: No ID found for update!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String sql = "UPDATE tbl_citizen SET c_fname = '" + cfname.getText() + 
                     "', c_lname = '" + clname.getText() + 
                     "', c_gender = '" + cgender.getSelectedItem().toString() +
                     "', c_email = '" + cemail.getText() +
                     "', c_address = '" + caddress.getText() +
                     "', c_status = '" + cstatus.getText() +
                     "', c_contact = '" + ccontacts.getText() + 
                     "' WHERE c_id = " + cid.getText();

        int result = dbc.insertData(sql);

        if (result == 1) {
            JOptionPane.showMessageDialog(null, "Citizen updated successfully!");
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Error updating data!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_saveMouseClicked

