    public void displayData() {
        try {
            dbConnector dbc = new dbConnector();
            Connection conn = dbc.getConnection();  // Get database connection
            
            String sql = "SELECT id, first_name, last_name, gender, contact, username, user_type, status FROM users WHERE id = ?";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, userId);
            ResultSet rs = pstmt.executeQuery();

            acc.setModel(DbUtils.resultSetToTableModel(rs)); // Populate table with user data
            
            rs.close();
            pstmt.close();
            conn.close(); // Close the connection
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error fetching user data: " + ex.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }
